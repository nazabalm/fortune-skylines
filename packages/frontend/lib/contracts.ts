// Helper to get contract address for current chain
export function getContractAddress(chainId: number): { refBoom: Address; usdc: Address } {
  switch (chainId) {
    case 84532: // Base Sepolia
      return CONTRACTS.baseSepolia
    case 8453: // Base Mainnet
      return CONTRACTS.base
    default:
      return CONTRACTS.baseSepolia // Default to testnet
  }
}

// Check if contract is deployed
export function isContractDeployed(address: Address): boolean {
  return address !== '0x0000000000000000000000000000000000000000'
}

